package ar.edu.unju.fi.dto;



public class MarcaDTO {
	private Integer id;
	private String nombre;
	
	
	public MarcaDTO() {
	
	}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
}
